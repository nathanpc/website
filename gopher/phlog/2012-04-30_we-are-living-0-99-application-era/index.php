		<h2>We Are Living The $0.99 Application Era</h2>
		<div id="published-date">2012-04-30</div>
	</div>

<p>I can still remember like it was yesterday, a time where paid applications never would cost less than $10. Today I can get on the App Store or the Play Store and download an fairly powerful app for no more than $3.99, but even at this prices I think twice before buying it. The App Store effect, as I like to call it, made the app consumer not want to pay more than $0.99 for a decent app, even if it's just $1.99 it might hesitate buying it.</p>

<p>I would comfortably pay more than $3.99 for a <strong>extremely</strong> well done, powerful and useful app. The problem is that those kind of applications aren't very common, but they are an expressive number, but I'm not here to talk about the apps that deserve this price tag, this article is about apps that aren't deserving their price tag.</p>

<p>In my opinion the best way to actually charge for an app is by using the freemium model, that's why <a href="http://www.fiftythree.com/paper">Paper</a> was such a success, you get the app for free and test it, if you like/need more features, in this case tools, you pay for them using in-app purchases, and if you want to unlock all the potential of the app you pay a discounted price for all the tools. This way the user can feel the app before buying it, which makes me hesitate when I need to buy the app without a way of testing it first.</p>

<p>On this model I'll start with an Android app called <a href="https://play.google.com/store/apps/details?id=com.teragadgets.android.notes">Flick Notes</a>, if you're a heavily <a href="http://simplenote.com/">SimpleNote</a> user like me you might know this app. It's an awesome clean and simple app, the problem is that in order to unlock all the (missing) features of the app: Note and notes list widgets, To-Do list style notes, and remove ads, you must pay a extremely expensive <a href="http://simplenote.com/">CA$4.99 fee</a>. I think this is to much expensive just to get rid of ads, enable to-do lists, and have the widget of the app. I would comfortably pay $1.99 for those features, and I'm sure the developer would earn a lot more money since a lot more people would buy the full version.</p>

<p>Another example of this, now on the desktop side, is a awesome new app for SimpleNote users that have a Mac OS computer, called <a href="http://www.metanota.com/">MetaNota</a>. It's a free, ad-supported app, but it's possible to remove it by paying a <a href="http://itunes.apple.com/us/app/metanota/id511648940">$9.99 fee</a>, yeah that's just to remove the ad.</p>

<p>These are just some small examples of what I'm talking about. If you're a developer that is planing to monetize your app in some way I suggest you to do the freemium way, but don't forget: We are living the $0.99 era! </p>

