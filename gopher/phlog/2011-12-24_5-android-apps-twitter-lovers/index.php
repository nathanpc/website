	<div id="blog-post" class="section">
		<h2>5 Android Apps For Old Twitter App Lovers</h2>
		<div id="published-date">2011-12-24</div>
	</div>

<p>
	Since the latest
	<a href="http://www.dreamintech.net/2011/12/a-faster-simpler-and-redesigned-twitter/">
	Twitter update (#letsfly)</a>, which tried to integrate the web apps and the
	mobile apps, but was a bit of a failure since they are aiming at the wrong
	direction. A great example of this is introducing the "Discover" tab (which
	was mislabeled, the correct label was "Ignore") and placing the Direct
	Messages in a bit of an obscure place in the middle of all the stuff on the
	"Me" tab. But the update wasn't all bad... The "Connect" tab is great, I can
	have all the mentions, retweets and follows on a single and organized place.
	Anyhow if you put everything into perspective it was a step back for
	Twitter.
</p>

<p>
	After this update a lot of people got really worried (like me) and started
	looking for alternatives to the official client. On iOS I've choose
	<a href="http://tapbots.com/software/tweetbot/">Tweetbot</a> which is the
	best app you can get on ANY mobile platform, even before the update it was
	better than the old app. But on Android there are a lot of Twitter clients
	available which makes it difficult to get the better one. To help you choose
	I searched and selected the best ones that can fit into different kinds of
	personality and tastes for Twitter clients.
</p>

<p>
	<img style="float: left;" alt="Twigee" width="124" height="124"
		src="https://web.archive.org/web/20120329155557im_/https://lh5.ggpht.com/SibGu6IENsyVfNi6JYXKrYeD8AFTlZBAZNWNpU5pzH_DkqzUeabk2xtzpzcx2_ptgVsV=w124">
	<b><a href="https://market.android.com/details?id=tice.twitterwalk">Twigee
	</a></b><br>This simple app has a lot of great features, a clean UI and
	shortcuts to actions like Reply, Retweet and Favorite. The app uses a very
	nice tabbed browsing that looks like the Android home screens. You don't
	have a TabBar, but an indicator of which "screen" you're in. The UI is in my
	opinion the best for a Twitter app on Android. The app widget to place on
	your home screen is great too. In my opinion this is a great app and it
	deserves more attention for sure.
</p>

<p style="text-align: right;">
	<img style="float: right; padding-left: 10px;" alt="Plume" width="124" height="124"
		src="https://web.archive.org/web/20120329155557im_/https://lh4.ggpht.com/6UYLkAFfkmt5kceSZahU_vOfpf8F3LtXI15et82TzF9eevM95XJT9MlTLRe2Op0O6As=w124">
	<b><a href="https://market.android.com/details?id=com.levelup.touiteur">
	Plume</a></b><br>If you're a long time Android user I'm sure you've
	already heard or used this client. It has a interesting UI, but it feels a
	bit disorganized, with all those colors and stripes everywhere, for me. The
	UX of this app is pretty good, a lot of shortcuts and it look and feels a
	lot like the old Twitter client for iOS (the most awesome official mobile
	Twitter client). It has a great selection of widgets to place on your home
	screen and have a more customized experience. If you have an Android tablet
	this is the app for you, which is one of the few apps (in all the
	categories) that is optimized for tablets.
</p>

<p style="text-align: left;">
	<img style="float: left;" alt="Tweetdeck" width="124" height="124"
		src="https://web.archive.org/web/20120329155557im_/https://lh6.ggpht.com/ygVHTsdGLXId6zzMGqDBQg_NzjaYJxD0PUrpyGTYkACOCixsdPArZIXynp8_8h2bWw=w124">
	<b><a href="https://market.android.com/details?id=com.thedeck.android.app">
	Tweetdeck</a></b><br>If you're a Windows user (and maybe a Linux user) you
	use/used this app on your desktop. The best way to describe this app is:
	It's just the desktop version changed a little to fit into a mobile screen.
	Personally I don't like this app because it's just a little version of the
	desktop app, there are almost no shortcuts and it's almost all about
	clicking some times to do simple things like retweet or reply, there are no
	shortcuts like swipe left or right to do actions, you need to click the
	tweet and it will take you to the single-tweet view and there will be
	buttons to do the simple actions, which I think isn't a smart thing. Anyhow
	I know a lot of people that love this app on Android and have their
	arguments to defend it.
</p>

<p style="text-align: right;">
	<img style="float: right; padding-left: 10px;" alt="twicaa" width="124" height="124"
		src="https://web.archive.org/web/20120329155557im_/https://lh3.ggpht.com/xF3hmKNu0fduPKyJKSO1qgc8XgAOqgtJ8w9DEfaKMeF6eKiucrFCRXahI-7GSF-DcNI=w124">
	<b><a href="https://market.android.com/details?id=jp.r246.twicca">twicca</a>
	</b><br>This app was made for clean and simple UI fans, it almost has a
	metro-style of UI. You have the options for white and black background,
	which is a great deal for a lot of people that like to have the choose for a
	dark-style because it's cool or just because you're reading at night. The
	point of this app is focused on the clean UI and how you feel really happy
	with the simplicity that the app has. After using it for 2 weeks i really
	loved the UI and it's great to "just read" your tweets after the breakfast
	when you browse the tweets you missed while you was sleeping.
</p>
	
<p style="text-align: left;">
	<img style="float: left; padding-right: 10px;" alt="Seesmic" width="124" height="124"
		src="https://web.archive.org/web/20120329155557im_/https://lh4.ggpht.com/84FN7CxV7Kuu6uso4qbAJ4Z37a9ABYYFw9lgS7ICLZoFqbN6-M8QjLsg6cTWrkHwIbo=w124">
	<b><a href="https://market.android.com/details?id=com.seesmic">Seesmic</a>
	</b><br>If you loved the old official Twitter client for Android UI you are
	going to love this app, since it looks a lot like it. There is a great
	selection of widgets and shortcuts to make your life easier while browsing
	your tweets. The "attached files" feature is awesome, which gives you a
	thumbnail of a photo that has it's link on a tweet and things like this,
	just like <a href="http://www.echofon.com/">Echofon</a> on a Mac. This is my
	client of choice and after using it for almost 1 month on my Galax Tab I
	can't be more satisfied.
</p>
	
