<p>
	Today was a very noisy day on my Twitter timeline. A lot of people talking
	about the new redesign and about how much they hated the new mobile apps for
	Twitter and etc. All this buzz around Twitter obfuscated the brand new
	mobile software that the search giant released today called
	<a href="http://www.google.com/producer/currents">Google Currents</a>. This
	news reading app is just like Flipboard, except it's available for iOS and
	Android. So all those Android users that were jealous about their iOS mates
	using Flipboard now have some real alternative on their OS.
</p>

<p>
	The app has it's phone version and has one which is optimized for tablets
	(Android tablets too). However you can only get it if you're in the US at
	the time. I have a US App Store account too and MarketEnabler on my Android
	phone so this wasn't a problem for me (which lives in Brazil). I must say
	that this is the first Ice Cream Sandwich optimized app that I test and it
	has a bit of lag and feels unoptimized on my Gingerbread devices, which
	might indicate that this is just a port of the ICS version for backwards
	compatibility. The app looks great on iOS, but Flipboard is still the best
	in my opinion.
</p>

<p>
	It's a great experience on iOS, but on non-ICS devices it's a bit painful.
	The lag and bugs makes your experience not so good and it seems to me a bit
	disorganized. Because of these things I won't get off from my dear
	<a href="http://www.pulse.me/">Pulse</a>. With Pulse I have all my feeds in
	a great design, organized and with more sharing options. Anyhow, if you were
	waiting for Flipboard to come to Android this is definitely an app for you.
</p>

<p>
	If you're a content-provider you can optimize your feed for the new Google
	platform using the <a href="https://www.google.com/producer/home">Google
		Currents Producer</a>, which is far lot easier than you think. The design
	interface is very simple and you can test how it would look on tablets,
	phones and on both of the supported OSes.
</p>

