<p>Since the first iPhone came out, in 2007, the world of touch screens went to capacitive because they were a lot more finger-friendly. Because of this people started to think of resistive screens almost as 90's technology and forgot that they were good at one key thing: Handwriting and pen recognition.</p>

<p>We, humans, are used to write and draw with pens, not with our fingers. This makes the experience with drawing, sketching, and note taking apps terrible. Some will say that anyone can buy a capacitive stylus that will work on all the modern mobile devices, the problem with those stylus is that they are not precise enough, they help a lot, but humans are precise when they want to draw or take some handwritten notes.Very often I take my HTC Touch Pro2 so I can take notes digitally.</p>

<p>At least this idea, of having a stylus on mobile devices, is still not dead yet. The Samsung Galaxy Note is the proof of this, also I want to mention that their stylus (S-Pen) implementation is very good, they managed to put a fairly precise stylus to work on their capacitive display. If other companies start to see this market and come out with great new ideas to merge the new technologies with the old ones I'm sure they will increase their profit significantly and will make their customers a lot happier. </p>

