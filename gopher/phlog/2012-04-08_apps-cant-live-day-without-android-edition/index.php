<p><em>This is the first article of a series I’ll be doing. Here I’m going to list the
		apps I can’t live a day without, in the various platforms I use on a daily
		basis.</em></p>

<p>I decided to start with iOS, which is the one I like most in the phone category,
	that’s why I always carry my iPhone with me: Apps. But then I choose to start with
	Android because in my opnion it’s a more productivity OS, also I’m writing
	this article on my Eee Pad Slider using Google Docs.</p>
<p>Let me begin by listing the Android devices I own and use every day:</p>

<ul>
	<li>ASUS Eee Pad Slider</li>
	<li>Samsung Galaxy Tab (the original 7″ version)</li>
	<li>Samsung Galaxy S</li>
</ul>

<p>I also have a Motorola Milestone (aka OG Droid) which I only use to test the apps I
	develop for Android. So I think it doesn’t count as one of my daily drivers. Now
	let’s begin with the app list:</p>

<p><strong><a
			href="https://web.archive.org/web/20120512170225/https://play.google.com/store/apps/details?id=com.google.android.music">Google
			Music</a></strong>
				<img title="Google Music"
				src="https://web.archive.org/web/20120512170225im_/https://lh3.ggpht.com/VkQhou4IFGmH42H5ZBgI2RkndYgJZ0GZ0Hk-zRWfwU0P7bi8WaI18xiwT59DRunDmhs=w124"
				alt="" width="124" height="124"><br>
	The best way to listen to my library whenever I go. It syncs my iTunes library
	with all my devices using the awesome Google cloud and albums that I like most and
	want to enjoy don’t matter if I have a internet connection, with a single tap and
	they get downloaded to my device so can be played offline. With a stuning UI and
	great features it’s by far the best music player for Android in my opinion. If you
	don’t want the cloud features you can check out DoubleTwist which a great
	alternative for people that just want their music on the device.</p>

<p style="text-align: right;"><strong><a
			href="https://web.archive.org/web/20120512170225/https://play.google.com/store/apps/details?id=com.github.mobile.gauges">Gaug.es</a><img
			class="alignright" title="Gaug.es"
			src="https://web.archive.org/web/20120512170225im_/https://lh6.ggpht.com/8QmrGjf-MZl0g8Zyv-3XP7OXpz0JrOwsyZVh3a6L84HJy-WAwzMDfnB1EpEJhSYlE1LH=w124"
			alt="" width="124" height="124"></strong><br>
	In my opinion the best analytics service available. It’s simpler than Google
	Analytics and everything happens in real-time. The app shows all the information
	you can have on the desktop version and also lets you have the awesome AirTraffic
	Live feature that shows you in real-time, on a map, the detailed traffic on your
	site. Since I love to check out every second how my sites are performing I can’t
	live without it for a day.</p>

<p><strong><a
			href="https://web.archive.org/web/20120512170225/https://play.google.com/store/apps/details?id=com.wunderkinder.wunderlistandroid">Wunderlist</a></strong>
			<img class="alignleft" title="Wunderlist"
				src="https://web.archive.org/web/20120512170225im_/https://lh3.ggpht.com/psTBUUZzx6Z1-U7Xd0A2oR8jy0_DwZIVrbTsHN4M5qBNYc9PXGB4gBNibtoULJolb2k=w124"
				alt="" width="124" height="124"><br>
	This is just the best task manager app ever invented. With a set of apps for every
	platform you can imagine from desktops to mobile devices. I use it to manage all
	the tasks I have to finish regarding my projects and keep them all synced across
	my devices. Definetly a must-have app for people that must be productive.</p>

<p style="text-align: right;"><strong><a
			href="https://web.archive.org/web/20120512170225/https://play.google.com/store/apps/details?id=com.google.android.apps.docs">Google Docs<img class="alignright" title="Google Docs"
				src="https://web.archive.org/web/20120512170225im_/https://lh5.ggpht.com/7ANoN-qYRjuEKiYdpv2N0ssLQdrBvDKN4wi6d4f_nlwozqzz4NsaPiHMrOWL1Hx16-Y=w124"
				alt="" width="124" height="124"></a></strong><br>
	The most important app on my Eee Pad Slider. Everyone knows this app, if doesn’t
	know at least the service you should know. I use it on a daily basis to write my
	articles and long texts for work or school. I stoped using any office suite, this
	includes iWork and Microsoft Office, after switching to Docs last year. Don’t
	matter where I go I can view and edit any document I want.</p>

<p><strong><a
			href="https://web.archive.org/web/20120512170225/https://play.google.com/store/apps/details?id=com.ideashower.readitlater.pro">Read it Later</a></strong>
			<img class="alignleft" title="Read it Later"
				src="https://web.archive.org/web/20120512170225im_/https://lh6.ggpht.com/sLQupGKNhTPFjgqTKHcwuEInVmOl8ksetmpyeD-gxboCCpmKHyEtdLa_iTk2Q5IXkA=w124"
				alt="" width="124" height="124"><br>
	I choose it instead of Instapaper because of the official support for Android and
	iOS. I love this app because I can keep all the articles I want to read offline
	and synced. Definitely a must-have for people that like to read a lot of articles,
	from Hacker News to The New York Times, on a day.</p>
