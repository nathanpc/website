<p>
	For those that don't know what Enyo is, here's their description at the
	<a href="http://enyojs.com/">official project site enyojs.com</a>:
</p>

<blockquote>
	Enyo is an open source object-oriented JavaScript framework emphasizing
	encapsulation and modularity. Enyo contains everything you need to create a
	fast, scalable mobile or web application. At the heart of Enyo is a simple
	but powerful encapsulation model, which helps you factor application
	functionality into self-contained building blocks that are easy to reuse and
	maintain.
</blockquote>

<p>
	If you remember WebOS the tool used to create the apps was Enyo. It was a
	awesome library and <a href="http://stackoverflow.com/questions/7155056">my
		dream</a> was to have it for creating apps for other platforms, and now this
	dream has become true. When I first saw Enyo I thought "This is the future
	of mobile and web app development". Some of my friends say that Enyo is just
	another jQuery-like framework for Javascript, but it's not.
</p>

<p>
	Enyo is not just another jQuery-like framework, it's much bigger. I've been
	using jQuery for about 2 years and almost the only things I use it for are:
	UI behaviors (like fade, removing, menusâ€¦) and simple AJAX. Sometimes I use
	a plugin like jFeed for handling RSS, but that's all. Enyo is a complete
	framework from UI to AJAX to events, it's almost a new language, you don't
	even need to code HTML and style with CSS, just do it all using Enyo, like
	if you were using <a href="http://www.sencha.com/">Sencha</a>.
</p>

<p>
	The announcement that Enyo was going open-source and working on desktops and
	mobile was great, but we'll need to wait until the awesome UI widgets we saw
	on the Touchpad come to open-source. Looks like it will be very fast and we
	won't need to wait a lot. According to the Enyo web site FAQ:
</p>

<blockquote>
	<b>When are the UI widgets coming to Enyo 2?</b>
	<br><br>
	It is the very next thing we are working on, and we hope to have it out in
	about a month. We'll update you with regular builds as work progresses, now
	that Enyo is an open source project.
</blockquote>

<p>
	I think this will change the way we develop apps for mobile once again. I'm
	already learning more about it so I can get rid of jQuery on my future
	Phonegap projects. What's your opinion about it?
</p>

