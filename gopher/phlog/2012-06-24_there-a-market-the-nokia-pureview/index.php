<p>Today I remembered about the Nokia PureView because of <a href="http://www.theverge.com/2012/6/23/3113024/nokia-808-pureview-sample-photos-video">The Verge's sample pictures and videos</a>, and I after thinking about the concept of that phone for a while I finally understood the potential market, besides their fan base, that they might be aiming at with that device.</p>

<p>First of all when Nokia unveiled the PureView at <a href="http://www.engadget.com/2012/02/28/the-engadget-interview-nokia-ceo-stephen-elop-at-mwc-2012-vide/">this year CES</a> my first impression was really bad, since I'll never ever understand camera phones. Also it was running Symbian, which for sure was the best choice since Microsoft would never let them customize Windows Phone to their pixel needs and because this was really on development for at least 2 years. Another thing that I noticed was that it was extremely thick, making it really a camera also does phone calls. This happened because of the entire camera mechanics they used, which impressed me a lot because of the way they did it, by inverting the way the lens moves instead of just doing like any point-and-shoot camera, in and out, they made the lens go up and down, removing the need of that popup lenses.</p>

<p>But now I saw that this product was targeted at people like for example journalists (more specifically from the independent media) so they wouldn't need to carry a camera with them while they are at a conference or just out gathering information for their article. I realised this after I started thinking about how many times I saw journalists using iPhones/point-and-shoot cameras to capture photo, video, or audio for their job.</p>

<p>I hope Nokia can get some attraction to this new device because I really liked it after realizing this. </p>

