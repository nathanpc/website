<p>Recently I was looking for a new mouse to replace my <a href="http://www.apple.com/magicmouse/">Apple Magic Mouse</a> since for some crazy reason it just doesn't want to work on my <a href="https://www.system76.com/laptops/model/bonx7">System76 Bonobo Extreme</a> (I went back to Linux), despite the fact that almost every person on the internet says it does. After searching for a while (2 months) I thought the best option was the <a href="http://www.amazon.com/gp/product/B0082D5660/">Logitech Wireless Anywhere Mouse MX</a>, so I ordered one.</p>

<p>I've been using it for 3 days now and I gotta say, it's a perfect mouse, the weight is great (about the same as the Magic Mouse), the scrolling wheel is ok, and it has two customizable buttons (I use them to change tabs in my text editor while I'm coding), the only complaint that I have is the fact that Logitech took a huge effort to come up with the most obnoxious name they could come up with for a mouse.</p>

<p>Some people may hate the fact that it doesn't use Bluetooth, which means you have to carry that little USB receiver with it, but the good thing is that there is place to put it in the battery compartment so you won't loose it, personally I don't care about wireless mouses that use the separate receiver.</p>

<p>Another great thing about it: It comes with a nice pouch to carry it in your backpack and protect it from scratches.</p>

<p>In a nutshell, it's a great mouse and if you're looking for one you should definitely take it into consideration. </p>

