<p>This is my thoughts about the new iPhone that Apple just introduced today. I'm writing this as I watch the keynote. (it was written yesterday at night so I decided to publish it today. I haven't reviewed or modified it.)<h1>iPhone 5</h1><h2>The name</h2></p>

<p>Seriously Apple? I really loved that you called the iPad 3 the new iPad, which is a lot better for you and for the people since now most of what you're going to do is just do some iterations like you did from the iPhone 4 to the iPhone 4S. Calling it the iPhone 5 makes no sense.<h2>The bigger screen</h2></p>

<p>That looked like a nice addition in the beginning, but it will become a hell. A lot of apps are going to have those crappy bars, could you guys do what Android does and just re-align the pixels? Most of the apps really have no reason to "take advantage" of that bigger screen. Also that aspect ratio is just the worst one ever. It will also start a little more fragmentation to the platform.<h2>That back</h2></p>

<p>Seriously that back looks like a generic chinese phone back. It is just so ugly. I would prefer if it was made all of the same color/material which would make a lot more sense.<hr /><h1>Stock Apps</h1><h2>Shared Photo Stream</h2></p>

<p>Haha, I had to laugh when I saw this. That's really the worst thing ever. They forgot that today our friends have different devices, usually they are half iOS users and half Android users, also Instagram does this a lot better and in a lot better and more social way.<h2>Siri</h2></p>

<p>Nice new additions to Siri (I'm talking about the sports stuff). I hope that's international because if that's only for the USA you just made a stupid decision.<h2>iTunes</h2></p>

<p>The new additions to iTunes were really awesome. The new Mac app UI is really great. I loved the clean interface.<hr /><h1>iPod</h1><h2>The nano</h2></p>

<p>The new iPod nano is really awesome and a lot better than the current generation. If I didn't used my phone as my music player I would buy it for sure. By the way I still can't understand why someone would use the iPod nano to watch a movie or look at photos.<h2>The touch</h2></p>

<p>The back really looks like crap. It's really thin and light, but the only market for that is kind of device is for children that their parents doesn't want to give them a smartphone. Also I had a "WTF?!" moment when they showed that wrist wrap they are calling iPod touch Loop.<hr /><h1>Conclusion</h1></p>

<p>Sadly Apple haven't got me back. I'll continue to develop for BlackBerry, I think they are doing a better work to innovate with BlackBerry 10. </p>

