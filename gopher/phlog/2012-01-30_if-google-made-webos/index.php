<p>
	After all the news of almost-dead WebOS now open-source within Enyo, which
	I've already
	<a href="http://www.dreamintech.net/2012/01/hp-enyo-has-joined-the-movement/">
	expressed my love for it</a> on a earlier post, I got me thinking "If Google
	made WebOS instead of Android and Palm made Android instead of WebOS, how
	the mobile market would be?"
</p>

<p>
	In my opinion if Google pushed WebOS to the market it would be almost an
	instantaneously success with lots of people getting it around the world and
	the world press talking about it (like what happened with the first iPhone),
	instead of the slow growing popularity on the beginning. People would fell a
	lot better about the interface and the experience from WebOS than on the
	first versions of Android. You can't denial that the first versions of
	Android were very bad and with a terrible user experience, even Gingerbread
	had a lot of problems when it comes to UX.
</p>

<p>
	But on the Palm Android side, things would have been a lot worst and it
	would be dead in 2010. The only thing that kept Android alive was Google
	integration and Google's power over everything. The fact of it being
	open-source was just an attractive for the hacker community, not for the
	average person.
</p>

<p>
	<b>TL; DR:</b> Palm Android dead in 2010 and Google WebOS the most
	successful mobile OS ever, maybe even more than the iPhone, in a shorter
	period than it's taking.
</p>

