		<h2>5 Apps To Be More Productive On Android</h2>
		<div id="published-date">2011-09-20</div>
	</div>

<p>
	On this post I'm going to discuss 5 applications for you to be more
	productive on your Android phone, since the only reason for me to keep using
	my Galaxy Tab instead of my iPad is because I'm a lot more productive on it.
</p>

<p>
	<b>
		<a href="https://market.android.com/details?id=com.evernote">
			Evernote
		</a>
	</b>
	<br>
	This is an awesome app and might be known by a lot of people. With it you
	can take notes and sync them across all kind of devices from Android, iOS,
	all the other mobile systems (even Windows Mobile) and computers running
	Linux, Mac OS X or Windows.
</p>

<p>
	Think of it like a big folder with sub-folders where you can put notes,
	images, audio, video, documents from Microsoft Office, PDFs and you can read
	or download anywhere don't matter the platform you're running. All you need
	is internet access.
</p>

<p>
	<b>
		<a href="https://market.android.com/details?id=com.wunderkinder.wunderlistandroid">
			Wunderlist
		</a>
	</b>
	<br>
	This awesome app will help you with your daily tasks and will sync them all
	with the cloud and will keep all your devices synced. The company offers
	versions for all the big mobile OSes and computers too.
</p>

<p>
	If you're coming from iOS, this is just like Reminders in iOS 5, but you
	cross-platform, which is great if you have more than one device or have
	plans to change your platform.
</p>

<p>
	<b>
		<a href="https://market.android.com/details?id=com.dropbox.android">
			Dropbox
		</a>
	</b>
	<br>
	Of course the cloud is the future, today we usually have a computer, a
	laptop, a tablet, a smartphone, and keep your files organized between them
	all can be a very difficult task. That's why Dropbox was created.
</p>

<p>
	With this awesome tool you can sync files between all your devices. For
	example you can start writing a document for a presentation at your
	computer, while in the traffic you pick up your tablet and finish it there,
	while on a coffee shop you realize that a some images will be great and you
	pick up your phone Google a good image and insert it into your document and
	after all the latest version will be always available on every device you
	want.
</p>

<p>
	If you just want to keep simple files in sync you can stay with the basic
	plan which is free, but if you want more space they offer 3 options: 50GB
	for $9.99/month, 100GB for $19.99/month and a team plan that can be
	customized to fit your needs.
</p>

<p>
	<b>
		<a href="https://market.android.com/details?id=com.alphonso.pulse">
			Pulse
		</a>
	</b>
	<br>
	If you're that type of person that can't live without your RSS feeds from
	Google Reader this is really the app for you. With a very well-designed UI
	and a great user experience to browse your news I can say for sure that this
	is the best RSS reading app available for Android.
</p>

<p>
	With it you can separate your RSS feeds by categories and each one will have
	a tab/page to keep everything more organized. The thumbnail view and the
	scrolling is great to browse your feeds.
</p>

<p>
	You can link your Google Reader account with it to get all your feeds, but
	you can browse their great selection of RSS feeds and find something new
	that you might like. You can also link your social network accounts and get
	a feed of the activity from them.
</p>

<p>
	<b>
		<a href="https://market.android.com/details?id=com.opera.browser">
			Opera Mobile
		</a>
	</b>
	<br>
	As many might know, the stock Android browser isn't the best one on Android,
	some users love
	<a href="https://market.android.com/details?id=mobi.mgeek.TunnyBrowser">
	Dolphin HD</a>, but for me Opera Mobile is the best one, maybe because I was
	a hardcore Windows Mobile user before my move to Android.
</p>

<p>
	If an awesome UI and a full web experience and no big problems you can
	browse the web in style on your Android device now. It also includes the
	Opera cloud system that lets you sync bookmarks and settings between your
	devices and you computer.
</p>

