<p>Three days ago a friend, which is a regular user, asked me about my opinion on the "new button-less phone that was just released", he was talking about the <a href="http://www.theverge.com/products/xperia-z/6490">Sony Xperia Z</a>. First I explained him that those kinds of phones (button-less) existed for a long time before the Sony one, then I gave him a long explanation why I think the Android way of getting rid of buttons was just wrong.</p>

<p>If you really want to get rid of the physical buttons you shouldn't replace them with virtual ones, since I still loose part of my screen to that piece of the past, so don't get rid of them, I prefer to have something more "natural" than just an abstraction of it.</p>

<p>To get rid of the physical buttons in the right way you should first of all replace their place with pixels (shocking!), so I can see more content. After that you replace the button actions to be triggered by something that won't take more screen space, for example gestures.</p>

<p>A clear example where a company made the transition perfectly is RIM, they came from a OS that was completely driven by physical buttons (BBOS) and went to a fully gesture driven OS (PlayBook OS and BB10). Another great example of how to use gestures is the awesome Ubuntu Phone which in my opinion is one of the best implementations I ever seen.</p>

<p>So, if you want to replace the buttons you shouldn't just virtualize them, but really replace them with something different. </p>

