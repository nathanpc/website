<p>
	This will be a little rant from a tech savvy person that lives on countries
	called "new markets". The term "new markets" means countries that are in 
	evelopment and their population can buy more. These markets are the best for
	a lot of industries like car manufactures, domestic equipments and
	technology industries in general, but looks like the mobile industry don't
	care about us.
</p>

<p>
	For example I am from Brazil and here we have 4 major mobile carriers: Vivo,
	Claro, Tim and Oi. I was looking for a new phone (the HTC HD7S), so I went
	to all those carrier shops to know if they had the phone to sell me and I
	had a surprise: None of the brazilian carriers have HTC current-models (just
	some of the old ones) because the company isn't interested on getting their
	devices here. But if you're looking for an iPhone or a Galaxy S (I or II)
	it's very very easy to get one. (Also, you can't buy WP7 phones here in
	Brazil because of some dumb government restriction)
</p>

