<p>Interpreted languages are pretty popular since 2005. <a href="http://rubylang.org/">Ruby</a> with the <a href="http://rubyonrails.org/">Rails</a> framework (<a href="http://blog.alexmaccaw.com/rails-is-just-and-api-and-that-s-ok">or API</a>), <a href="http://www.python.org/">Python</a> with <a href="http://www.djangoproject.com/">Django</a>, Javascript with <a href="http://nodejs.org/">Node.js</a>. These are just some examples of interpreted languages that became extremely popular, mostly because of frameworks, but the main thing that will make the future be ruled by interpreted languages aren't frameworks. Instead the best feature of these languages are the fact that you can easily test or debug your code on-the-fly, without having to worry about compiling a test source code to see if it works.</p>

<p>The Node.js console for example: I can run my server script on the test machine, then I type node and I can debug/test the code I just wrote, sending GET/POST requests, doing simple database queries (<a href="http://mongodb.org/">MongoDB</a> of course) and testing code I might add to the main script. A lot of people complain that they are slow because of the way they "read" your code, but seriously I never had any problem with slowness on my code, so I think this isn't an issue.<br /><br />Of course languages like C/C++ (I hope <a href="http://golang.org/">Go</a> can kill them because they are completely outdated and there is no more reason to continue using them) or Java will continue to exist for a long time because there are some things, like bootloaders, security systems, OSes, that can only be written in code that can be compiled.<br /><br />I hope the future gets even brighter for interpreted languages, that's why I'm trying to give them all a try before choosing the one I'll stay. At the time Node.js is the best for me because I really love Javascript, but I might get into Ruby too, I like the syntax and the community is really great too. </p>

