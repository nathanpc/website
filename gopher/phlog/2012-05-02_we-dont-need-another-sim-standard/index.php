<p>Three days ago I received my first BlackBerry development device, a Torch. As I said, it will be just a development device, so I have to use it for a while to learn how the apps look like and how they feel, so I can start to develop/port applications to the OS. Why I told all this story? Simple, I use a microSIM on my iPhone, so I had to purchase a converter to use it on my BlackBerry, because it's the SIM my carrier automatically activated the BlackBerry Internet Services. The day I had to buy the converter I remembered the new <a href="http://www.theverge.com/2012/5/8/3007535/nano-sim-4ff-apple-modified-vote-mid-may">nanoSIM project</a> and thought: "The problem isn't SIM design, it's the SIM itself".</p>

<p>We don't need another <a href="http://en.wikipedia.org/wiki/Subscriber_identity_module">SIM</a> design, we need to get rid of the SIM. It's a 1998 that just got little updates over time. We are moving everything to the cloud (I know a lot of people hate this term, but I don't care, I like it), our contacts, files, photos, our entire lives, why not all the informations the carrier needs to authenticate our plan?</p>

<p>The idea is fairly simple: Just as I have to register a username and password to have BIS (BlackBerry Internet Services) with my carrier, just make this for everyone on the carrier, as soon as you get your first phone/plan you register a username/password and all your information gets stored on the carrier. When you turn your phone ON, it connects to the carrier and ask you for the credentials, if they are valid it will download all the information and get your plan up and running.</p>

<p>What's your opinion about this idea? Any thoughts about this topic? Leave a comment, I love to read and respond to them. </p>

